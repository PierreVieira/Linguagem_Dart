main(){
  // Aritméticos
  int a, b, resultado;
  a = 7;
  b = 3;
  resultado = a + b;
  print(resultado);
  print(a - b);
  print(a * b);
  print(a / b);
  print(a % b);

  //Operadores Lógicos
  bool fragil, caro;
  fragil = true;
  caro = false;
  print(fragil && caro); //E
  print(fragil || caro); //OU
  print(fragil ^ caro); //OU Exclusivo
  print(!fragil); //NOT
}